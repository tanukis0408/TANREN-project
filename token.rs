#[allow(dead_code)]
#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    // Литералы
    Int(i64),
    Float(f64),
    Str(String),
    Bool(bool),
    Nil,
    Identifier(String),

    // Ключевые слова
    Fn,
    End,
    If,
    Elif,
    Else,
    For,
    In,
    While,
    Loop,
    Return,
    Let,
    Say,
    And,
    Or,
    Not,
    Use,
    Match,
    Break,
    Next,
    Struct,
    Class,
    True,
    False,

    // Операторы
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Power,
    Eq,
    EqEq,
    NotEq,
    Less,
    Greater,
    LessEq,
    GreaterEq,

    // Скобки и разделители
    LParen,
    RParen,
    LBracket,
    RBracket,
    LBrace,
    RBrace,
    Comma,
    Colon,
    Dot,
    DotDot,
    Arrow,
    Pipe,
    Bar,

    // Специальные
    Newline,
    Eof,
}